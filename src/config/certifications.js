// Função para obter todas as certificações em ordem de importância
export function getAllCertifications() {
  const all = [
    ...certifications.tecnologia,
    ...certifications.ia,
    ...certifications.idiomas,
    ...certifications.ferramentas,
    ...certifications.softSkills,
    ...certifications.outros
  ];
  
  return all.sort((a, b) => a.importancia - b.importancia);
}

